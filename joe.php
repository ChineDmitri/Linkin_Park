&nbsp&nbsp&nbspРодился 15 марта 1977 в городе Глендейл, штат Калифорния. В настоящее время живет в Лос-Анджелесе. Джо женат, его избранницей стала Карен Бенедит, с которой он встречался долгое время, и они поженились только в феврале 2006. Джо по национальности кореец. Его родители переехали из Кореи в Америку, и где в скорое время родился Джозеф. Джо самый младший в семье, у него есть две старшие сестры.</p>
<p>&nbsp&nbsp&nbspОн закончил Hoover high school, затем поступил в школу искусств города Пасадены, но так и не закончил её. Так же там он познакомился с Майком. Джо и Майк, специализировались в иллюстрировании. Но так же их обоих интересовало рисование и Джо остался в колледже на год больше, чтобы заняться фильмами. Он является режиссёром практически всех клипов Linkin Park. Mr.Hahn работал над спецэффектами для сериала &quot;Секретные материалы&quot;. Он увлекался оптическим искусством. Джо и Майк вместе нарисовали обложку дебютного альбома Linkin Park &quot;Hybrid Theory&quot;. У Джо есть свой собственный магазин одежды в Лос-Анджелесе - Suru. На одной из витрин магазинчика установлены DJ'ские вертушки. Джо Хан время от времени играет на них в магазине.</p>
<p>&nbsp&nbsp&nbspДжо обожает прикалываться над репортерами, которые два слова связать не могут: так мистер Хан прикольнулся во время интервью перед московским концертом 2007-го года молчал всё время смотрев в одну точку в это время пытался говорить Майк, терпя неграмотность русского репортера канала A-ONE, в конце концов неожиданно Джо рявкнул на него и без того еле говорящего по-английски сотрудника, в тот момент очень долго смеялся Майк (смотрим онлайн этого интервью по <a title="Интервью канала A-One" href="http://kiwi.kz/watch/qriqlqyb320o">этой ссылке</a>). У Джозефа, как и у Майка, нет ни одной татуировки или пирсинга. Натуральный цвет его волос-чёрный, но он время от времени перекрашивает их в светло-каштановый. Перед тем как 28-летний Джо Хан стал одним из Linkin Park, он был начинающим художником комиксов. Хан встретился с журналистом из Wizard, чтобы окинуть взглядом свою карьеру, свою любовь к комиксам, и что могло, бы получится.</p>
<p>&nbsp&nbsp&nbspТакже как Честер и Майк, Джо имеет собственные проекты. Он так же снял в Лос-Анджелесе мини-фильм &quot;The Seed&quot;. Джо работал над фильмами &quot;Kung Fu High School&quot; и &quot;King Rat&quot;, которая является адаптацией романа Чина Мивелля. Книга "King Rat" была предоставлена Хану его личным агентом.</p>
