Биография Майка Шиноды (Клавишные, Струнные, Вокал).
</p>
<p>
<img align="left" src="images/bio/mike_and_anna.jpg">
&nbsp&nbsp&nbspМайкл Шинода родился в 1977 году 11 февраля в Агауре, штат Калифорния, но в настоящий момент живёт в Лос-Анджелесе, район Woodland Hills. Майк женат. Его избранницей стала Anna Marie Hillinger, дата брака — 10 мая 2002. Известно, что Анна и Майк встречаются почти с колледжа. У Майка есть брат, которого зовут Jason (Хотя он называет его Jay). Майк — Yonsei, это означает, что он принадлежит к четвёртому поколению японцев в своей семье, родившихся в Америке.</p>
 <p>&nbsp&nbsp&nbspОн учился в средней школе Агауры, он получил высшее образование в Художественной школе Пасадена. Его специальность - дизайнер. Он хорош в иллюстрации, но усердно изучал графический дизайн. Сразу после колледжа он стал работать по своей специальности. Обложки почти всех пластинок Linkin Park нарисованы лично его руками. Десять лет Майк брал уроки классической игры на фортепиано. Первый опыт в написании музыки у Майка появился в 3 года, когда мама записала его в класс игры на пианино, а в 12 он впервые в жизни написал нечто полноценное для конкурса игры на пианино, на котором, он, к своему удивлению и гордости, занял первое место, причём соревновался Шинода с конкурсантами на 5 лет старше него самого</p>
 <p>&nbsp&nbsp&nbspМайк: <i>Впервые в жизни я осознал, что я могу писать песни, это талант и ведь писать песни, это весьма прикольное занятие</i></p>
<p>&nbsp&nbsp&nbspЗа это время ему порядком поднадоела классическая музыка, таким образом он увлекся джазом, а потом и hip-hop'oм. Скоро Майк начал придумывать собственные рифмы и ради смеха прочитывал их своим друзьям. Майк умеет играть на гитаре. И в последнее время он в группе пробует себя в роле гитариста.</p>
<p>&nbsp&nbsp&nbspМайк слушал таких исполнителей как: Run DMC, Eric B AndRakim,Grandmaster Flash и LL Cool J. Альбомы, которые слушал чаще всего в 2000-ом году: Deftones - "White Pony" , The Roots - "Things Fall Apart" ,Nine Inch Nails - "The Fragile" , Black Eyed Peas - "Bridging The Gap" , Incubus - "Make Yourself" , System Of A Down - "System Of A Down" , Dido - "No Angel" , Jurassic 5 - "Quality Control" , Stone Temple Pilots - No.4" и Eminen - "The Marshall Mathers LP".</p>
<p>&nbsp&nbsp&nbspПо характеру он очень добрый, с чувством юмора, ответственный, трудолюбивый. Вместе с Мистером Ханом периодически прикалываются над окружающими, являются главными тусовщиками группы. Ещё он обожает говорить о себе от третьего лица. Фирменные фразы — «you know», «kinda like that». Шинода и дня не может прожить без горячей ванны или душа. Его раздражает то, что Брэд редко принимает душ. Любит восточную кухню, острое; если готовит сам, то это будет что-то в стиле гриль. Из гастрольных туров Майку очень понравилось в Малайзии; вернувшись оттуда, он был под большим впечатлением. Очень любит ром с колой и очень часто его пьёт. И ещё он обожает зелёные яблоки.
У Майка нет ни одной татуировки и пирсинга и курит, в отличие от Честера, только тогда, когда выпивает, причём сигареты никогда сам не покупает, а стреляет у окружающих. В ноябре 2005, Майк Шинода дебютировал как соло-артист со своим "The Rising Tied" в которую поместил амбициозную колекцию музыки. В создании (как продюсер) Jay-Z, который и раньше сотрудничал с Майком в 2004 году, проект носил нозвание "Collision Course". Песня <a title="Скачать Fort Minor - Kenji" href="http://ви">"Kenji"</a> расказывает нам про нелегкие отношения между Японией и Америкой во время Второй Мировой Войны. Запись трека проходила вместе с родствениками Майкла, они рассказывали про свой опыт в изоляционных лагерях.
</p>
<p>Говоря языком спортивных комментаторов, он провел точечную селекцию: неторопливо и придирчиво были отобраны достойные кандидаты на серьезные роли: Common, Jay-Z, Black Thought, John Legend, Ghostface Killah, Sixx John, Styles Of Beyond. Всё получилось так, как он хотел: с шумом, с помпой, с фанатами. Стоит особенно отметить, что за лаве не гнался, да и вообще человек не меркантильный.</p>
<p>Майк Шинода проделал путь от мальчика, влюблённого в рэп до уже сейчас маститого музыканта, известность таланта которого набирает всё большие обороты, неся свет из эфиров самых топовых телеканалов и радиостанций.</p>
