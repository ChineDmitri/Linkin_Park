Биография группы Linkin Park.
<center>
<img src="images/bio/linkinpark.jpg">
</center>
&nbsp&nbsp&nbspГруппа Linkin Park посвятила себя своей музыке и слушателям cмешав рок, хип-хоп и электронную музыку (не зря их первый альбом называетса Hybrid Theory) и слыша их песни мы легко запоминаим эти мелодии. В звучании лос-анджелесской группы мелодичный вокал сочетается с жесткими ритмами, тяжелыми гитарными риффами и головокружительными скрэтчами. В составе Linkin Park шесть человек: Роб Бурдон (ударные), Брэд Делсон (гитара), Даррен Фаррел (бас-гитара), Джозеф Хан (вертушки), Майк Шинода (MC, гатара, вокал) и Честер Беннингтон (вокал)</p>
 <p>&nbsp&nbsp&nbspРоб, Брэд и Майк познакомились еще в школе. Окончив учебное заведение, Майк входит в состав группы Xero, в которой также играют студент арт-колледжа и диджей Джозеф Хан и Дэйф Ферел. Поже Майк приглашает своих давних друзей Роба и Брэда. И в то время у них был вокалист Mark Wekefield. Потом группа меняет название на Hybrit Theory. И в 1998 году место Марка приходит на его место Честер Бенингтон и поже они меняют название на Linkin Park. Но точьней должно было быть название группы Linkoln Park в честь Аврама Линкольна но увы домен linkolnpark.com уже был занят вот и решили назвать группу таким именем которым по сей день она и называетса Linkin Park.</p>
 <p>&nbsp&nbsp&nbspГруппа начинала свое развитие записывая свои песни и публиковали на чюжих сайтах, расылали футболки с названием группы. И в скором времени группа получает успех в клубах Лос-Анджелеса и за его пределами. В конце 1999 года Linkin Park подписывают контракт с Warner Bros и начинают запись дебютного альбома. Они решают назвать диск “Hybrid Theory” в честь старого названия группы. Продюсером альбома стал Дон Гилмор. Диск вышел осенью 2000 года и был похож по звучанию на такие разные группы, как Deftones, The Roots, Nine Inch Nails и даже Aphex Twin. Такие синглы, как “Crawling” и “One Step Closer” быстро получили успех и постоянно звучали по радио и на MTV.  А “One Step Closer” стал вроде гимна фанатов группы. И с этой песни пошло подпольное фан сообщество которое в дальнейшем будет называтса LPUnderground (LPU).
 Они начинают постоянно концертировать, приобретая все большее число поклонников. В общей сложности в 2001 году группа отыгрывает 324 концерта! В январе 2002 года диск “Hybrid Theory” выдвигается на премию «Грэмми» в трех номинациях, включая «Лучший рок-альбом», а сами музыканты представлены в номинации «Открытие года». К радости фэнов группа завоевывает одну премию в номинации «Лучшая композиция в стиле хард-рок» за песню “Crawling”. А летом 2002 года музыканты выпускают свой второй альбом “Reanimation”, параллельно работая над следующим альбомом "Meteora", который вышел весной 2003 года. Пластинка “Meteora” повторяет успехи “Hybrid Theory”.
 Синглами с альбома становятся "Somewhere I Belong", "Numb" и "Faint", занявшие высокие позиции во всех чартах, кстати первый клип с альбома "Meteora" это Somewhere I Belong. В 2003 году Linkin Park были удостоены множества премий, в том числе и награды MTV Video Music Awards. В ноябре 2003 года Linkin Park выпустили ( Live - концерт ) комплект из компакт-диска и DVD “Live in Texas”. Материал для этого концертного альбома записывался в двух разных городах - в Далласе и Хьюстоне. В 2004 году группа устроила собственное масштабное турне под названием “Projekt Revolution”. По результатам 2004 года “Projekt Revolution” оказались самыми прибыльными гастролями года. В конце 2004 года по просьбе MTV группа записала совместную пластинку под названием “Collision Course” с рэппером Jay - Z, выполненную в технике “mash up”. Альбом “Collision Course” дебютировал сразу на первом месте чартов и принес группе «Грэмми».
 В 2005 году Linkin Park решили немного отдохнуть от активной к
 онцертной деятельности. За год они дали всего два концерта – один в рамках серии благотворительных выступлений “Live 8”, а другой для организации “Music for Relief”, которую сами музыканты и создали, чтобы помогать пострадавшим от различных природных катаклизмов. Но Майк в это время не спит он объявил о своем желании поработать над собственным хип-хоп-проектом Fort Minor. Дебютный альбом Fort Minor “Rising Tied” вышел в свет в ноябре 2005. Честер Беннингтон также создал сразу несколько побочных проектов, и вся эта деятельность на стороне породила массу слухов о распаде группы. Однако оба вокалиста подтвердили, что распускать Linkin Park никто не собирался. В начале 2006 года стало известно, что группа отправилась в студию работать над новой пластинкой. В качестве продюсера этого альбома был привлечен человек-легенда Рик Рубин, работавший со Slayer, Red Hot Chili Peppers и System of a Down.
 Работа над альбомом “Minutes to Midnight” растянулась на полтора года – вместе с Рубином группа засела в огромном особняке в Калифорнии и принялась экспериментировать с новыми звуками – в записи альбома использовались банджо, меллотроны, старинные гитары и усилители из личной коллекции Рубина и множество других, на первый взгляд, не подходящих для Linkin Park инструментов. Этой пластинкой сами музыканты надеются сломать сложившийся вокруг них стереотип.
</p>
<p>&nbsp&nbsp&nbspМайк: <i>Нас всегда записывали в одну кучу со всеми остальными рэп-рок артистами. Но мы сами никогда не стремились к такому положению вещей. У нас всегда было свое собственное лицо, и, полагаю, эта пластинка докажет это.</i></p>
<p>&nbsp&nbsp&nbspЗа столь продолжительное время работы над альбомом музыканты сочинили около 150 песен. Из-за такой обильной творческой активности группы выход пластинки переносился несколько раз – изначально он планировался на лето 2006 года, потом был перенесен на осень, потом на начало 2007 года, и, в конце концов, вышел в мае. В интервью, данном в сентябре 2006 года, Майк Шинода так объяснил эти переносы: «Когда песни буквально льются из тебя, совершенно не хочется как-то останавливать этот процесс». Из «налившихся» сотни с лишним песен группа отобрала для записи 17 штук – в альбом позже вошло только 12 из них.</p>
<p> &nbsp&nbsp&nbspНовый материал Linkin Park заметно отличается от всего, что они записывали раньше здесь почти нет сэмплов, и рэп-вокал Майка Шиноды можно услышать только в двух песнях. Сами музыканты считают “Minutes to Midnight” своей самой взрослой и серьезной работой когда либо.</p>
