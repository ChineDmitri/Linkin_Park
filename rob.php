&nbsp&nbsp&nbspРоб родился в городе Калабасас, штат Калифорния, 20 января 1979 года. На сегодня живет в  Лос-Анджелесе. Роб по национальности еврей. Улился в Agoura High School. У Роба была более 7 лет девушка Ванесса Эвиган. Ванесса - актриса, дочь актёра Грэга Эвигана, её сестра Брайана тоже актриса, снималась в клипе "Numb". Но в начале 2008-го года Роб заявил, что расстался с Ванессой. </p>
<p>&nbsp&nbsp&nbspОн начал играть на ударных ещё в начальном классе. Игра на ударниках требует большой координации и ловкости. Почти во всех песнях Linkin Park ведущим инструментом являются ударники, они в песне играют большую роль, задают темп песни и не дают ей распасться на части. Мать Роберта была знакома с Joey Kramer'oм барабанщиком Aerosmith. Впервые когда он и его друзья начали делать каверы, это было в 13 лет на такие композиции как Smells Like Teen Spirit" (Nirvana). Через некоторое время он и Брэд организовали группу под названием Relative Degree. Группа представляла собой смесь рока, фанка и рэпа, но потом они распались. Его первый профессиональный дебют состоялся в клубе "The Roxy".
<p>&nbsp&nbsp&nbspДо того, как стать музыкантом, Роб работал официантом. В детстве брал уроки классической игры на фортепьяно, сейчас любит поиграть на пианино в свободное от дел время. Робу нравится путешествовать, отдыхать на море, кататься на водных лыжах и сёрфинге, в общем, ведет здоровый образ жизни. Если сказать честно хотя Роб и знает нотную грамоту но он не играет по нотам он гораздо лучше играет, на свой слух, и это у него получается хорошо. В старших классах пристрастился с алкоголю и наркотикам сам Роб об этом говорит так:
<p> &nbsp&nbsp&nbsp<i>"Я не чувствовал, что вписываюсь в компанию своих школьных друзей, стал очень закрытым. Несмотря на это, я не прекращал играть на барабанах, но с совершенствованием игры были проблемы. Ближе к концу школы я вернулся в калею и снова начал серьёзно практиковаться в музыке, слушал множество фанка: Sly and the Family Stone, Tower of Power, Earth Wind and Fire, James Brown, Carter Beauford. Я всерьёз начал уделять внимание барабанной части музыки. Когда я снова начал полноценно играть, мне позвонил Майк. Он приехал из колледжа и гостил дома у своих родителей, спросил, не хочу ли я послушать несколько песен, которые он написал с Брэдом и другими нашими друзьями. Я согласился и был удивлён его работой, мне захотелось присоединиться к ним. Музыка была очень похоже на стиль, в котором сейчас играет Linkin Park, но между музыкальными элементами не было столь прочной связи.
Рок-части были отделены от хип-хоп-частей, которые в свою очередь были отделены от электронных сэмплов; это была скорее мозаика, чем гибрид, который мы имеем сейчас. Я всегда хотел заниматься музыкой, особенно играть на барабанах. Просыпаясь каждый день, я благодарен судьбе за то, что я могу заниматься тем, что мне нравится. Я признателен всем нашим фанатам за то, что сделали мою мечту реальность."</i></p>
