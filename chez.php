Биография Честера Беннингтона (Вокал).
</p>
<p>
<img align="left" src="images/bio/chez.jpg">
&nbsp&nbsp&nbspЧестер родился в Фениксе 20марта 1976 года, в штат Аризона. Сейчас живёт в Лос-Анджелесе вместе со своей женой Талиной Бентли. У честера 4 сына: Тайлера Ли 2006 года рождения, Дрейвен Себастьян Беннингтон 2002 года рождения, сын Джейми 1996-ого года рождения от одной из своих бывших подруг и усыновлённый Йен.</p>
 <p>&nbsp&nbsp&nbspВ своем родном городе до 1993 года Честер былвокалистом в группе Grey Daze. Честер выделяется громким, высоким и порой кричащим вокалом, который он использует постоянно во время пения. В детстве он часто доводил свое горло до ран. Первым музыкальным инструментом Честера был пианино. В юности Честер сменил много музыкальных групп и играл на множестве инструментов, но главным стремлением у него был вокал.</p>
 <p>&nbsp&nbsp&nbspДетство у Честера было нелёгким. Он испытывал сексуальные домогательства, несколько друзей совершили самоубийство а еще один друг умер из-за несчастного случая, катаясь на скейтборде. Родители Беннингтона развелись в 1987. Осталса он с отцом, двумя сёстрами и братом. В 11 лет Честер был самым младшим из четырёх детей а его отец был офицером полиции. До развода его семья часто путешествовала по Аризоне, по таким городам как Скотсдэйл, Толлесон, Темпе и т.п.</p>
<p>&nbsp&nbsp&nbspЧестер: <i>Раньше я был спортивным ребёнком, но как только я прекратил заниматься здоровым образом жизни, так сразу перестал преуспевать в школе, я начал курить травку и ходить на вечеринки. Кажется, мне было 11, когда я первый раз попробовал марихуану.</i></p>
<p>&nbsp&nbsp&nbspЧестер учился в Centennial High School, Greenway High School , и позже окончил Washington High School. Честер говорит, что в школе он был полным придурком. В юности работал в кофейне. Честер работал в Bean Tree Coffee House, он говорит, что это держало его в движении, и что кофейни были основным источником его вдохновления. Во много Честеру помогал его старший брат, именно он приобщил малыша Чеза к музыке. Сейчас он отвечает за официальный фан-клуб LPUnderground. Ещё он является поклонником творчества певицы Aaliyah. И песня System которую он записал для фильма Queen Of The Damned посвятил ей. </p>
<p>&nbsp&nbsp&nbspЧестер много курит, являетсябольшим любителем хорошего пива, в юности он употреблял наркотики, впоследствии он приобрел некоторые проблемы со здоровьем. Раньше он носил очки, без которых он ничего не видел, а в 2004-м году сделал себе лазерную операцию, после которой у него полностью наладилось зрение.</p>
<p>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp<u>У Честера на теле 13 татуировок:</u>
