<br>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 5. Японский карп на правом плече.
<br> 
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 6. Японский карп на левом плече.
<br> 
