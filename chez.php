<br>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbspСвою первую татуировку онсделал в 18 лет. На сегодня Честер имеет собственную сеть тату салонов, в которых сам является частым клиентом.
</p>
<p>&nbsp&nbsp&nbspВ 2005-м году у него появился сайд-проект Snow Whit Tan, в котором он играет на гитаре, клавишах, сэмплирует звуки и является автором многих текстов песен. Но Linkin Park для Честера, как и для других членов группы на первом месте. Он присоединился к Linkin Park в 1998 году, заменив Марка Вэйкфилда.</p>
<p>&nbsp&nbsp&nbspЧестер Беннингтон, не только талантливый музыкант, но и талантливый модельер. Одежда – это его страсть. По количеству обуви он может сравниться с Сарой Джессикой Паркер.Честер каждый раз долго выбирает одежду, которую наденет, придирчиво подбирая, подходит ли ремень к ботинкам, и гармонируют ли носки с нижним бельем. Неудивительно, что Беннингтон выпускает свою именную линию Rock&Roll'ой одежды. Бренд носит название Ve’cel. Сам Честер позиционирует свой бренд как одежду для обоих полов в возрасте от 15 до 40 лет, для тех, кому нравится панк-роковый стиль в одежде, и тех, кто ценит нетривиальный дизайн. Продукцию Ve’cel отличает высококачественная графика. Конечно, Честер Беннингтон участвует в разработке дизайна одежды для Ve’cel, но успех линии напрямую зависит от успехов группы Linkin Park. концертах. С продвижением одежды проблем возникнуть не должно - Linkin Park дают сотни интервью, их фото украшают обложки журналов
Хотя Честер все же надеется, что его бренд станет самодостаточным. Ведь одежда весьма высокого качества. Между тем Честер Беннингтон не упустит возможности прорекламировать свой продукт, надевая одежду своей марки на концертах. С продвижением одежды проблем возникнуть не должно - Linkin Park дают сотни интервью, их фотки украшают обложки модных журналов и т.п.</p>
<p>&nbsp&nbsp&nbspНа сегодня Беннингтон создает новый сольный проект под названием  Dead by Sunrise, в котором участвуют певцы группы Julien-K и много других рок музыкантов. Этот альбом обещает Честер будет чисты Рок.</p>
